#include "libft.h"

int	ft_isspace(char c)
{
	if (c == 32 || (c >= 9 && c <= 13))
		return (1);
	return (0);
}

int	ft_isblank(char c)
{
	if (c >= 9 || c == 32)
		return (1);
	return (0);
}
